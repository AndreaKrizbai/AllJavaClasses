package day03;

public class StringDateType {

    public static void main(String[] args) {

        String name = "Andrea";
        int age = 34;
        double height = 5.3;
        char myLastNameFirstChar = 'K';
        boolean isMarried = false;
        byte childrenCount = 0;
        String city = "Rockville";

        System.out.println("My name is " + name);
        System.out.println("My age is " + age);
        System.out.println("My height is " + height);
        System.out.println("My last name's first character is " + myLastNameFirstChar);
        System.out.println("I am married: " + isMarried);
        System.out.println("I have " + childrenCount + " kids");
        System.out.println("I live in " + city);

        System.out.print("My name is " + name + ".");
        System.out.print(" My age is " + age + ".");
        System.out.print(" My height is " + height + ".");
        System.out.print(" My last name's first character is " + myLastNameFirstChar + ".");
        System.out.print(" I am married: " + isMarried + ".");
        System.out.print(" I have " + childrenCount + " kids" + ".");
        System.out.print(" I live in " + city + ".");

    }
}
