package day03;

public class EscapeCharacter {

    public static void main(String[] args) {

        System.out.println("Hello \\ world");

        System.out.println("I like the book \"Java\" ");

        System.out.println("The movie name is: \'Lord of the rings\' ");

        System.out.println("Hello\tWorld");

        System.out.println("Hello\nB15");

        System.out.print("This is regular print \n");
        System.out.println("This is println");

        System.out.println("I \nL \nO \nV \nE");
    }
}
