package day06;

public class OddOrEven {
    public static void main(String[] args) {

        int myNumber = 10;
        int yourNumber = 11;

        System.out.println("My number's remainder of dividing by 2: "
                + myNumber % 2);
        System.out.println("Your number's remainder of dividing by 2: "
                + yourNumber % 2);
        System.out.println("Adding my number to your number is: "
                + yourNumber + myNumber);
        System.out.println("Adding my number to your number is: "
        + (yourNumber + myNumber));

    }
}
